/*Var
mesma coisa do let, só que mais antigo e menos utilizado e recomendado
*/



/*const
Um tipo de declarar uma variavel onde ela se mantém constante e imutavel, onde não se pode trocar o valor atribuido a ela*/
const numero = "99"



/*let
Um tipo de declarar uma variavel onde se pode trocar o valor dela durante o programa
*/
let texto = "olá, mundo"

texto ="se lascar"

alert(texto)

alert(numero)



